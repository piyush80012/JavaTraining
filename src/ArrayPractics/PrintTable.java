package ArrayPractics;

public class PrintTable {
    public static void main(String[] arg){
        int table = 2;
        for(int i = 1; i <= 10; i++){
            System.out.println(table + "*" + i + "= "+table*i );
        }


        int print = 5;
        for(int i=1; i <=10; i++){
            System.out.println(print + "*" + i + "= "+print*i );
        }
    }
}
