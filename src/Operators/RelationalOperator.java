package Operators;

public class RelationalOperator {
    public static void main(String[] args) {
        int a = 8;
        int b = 7;

        System.out.println("a<b"+(a<b));
        System.out.println("a<=b"+(a<=b));
        System.out.println("a>=b"+(a>=b));
        System.out.println("a==b"+(a==b));
        System.out.println("a!=b"+(a!=b));
        System.out.println("a>b"+(a>b));
    }
}
