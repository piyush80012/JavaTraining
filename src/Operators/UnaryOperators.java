package Operators;

public class UnaryOperators {
    public static void main(String[] args) {
        int a = 5;
        int b =65 ;
        int c = 56;
        System.out.println("Postincriment:" +(a++));;
        System.out.println("Postincriment:" +(b++));;
        System.out.println("Postincriment:" +(c++));;
        System.out.println("Preincriment:" +(++a));;
        System.out.println("Preincriment:" +(++b));;
        System.out.println("Preincriment:" +(++c));;

        System.out.println("Postdecrement:" +(a++));;
        System.out.println("Postdecrement:" +(b++));;
        System.out.println("Postdecrement:" +(c++));;
        System.out.println("Predecrement:" +(++a));;
        System.out.println("Predecrement:" +(++b));;
        System.out.println("Predecrement:" +(++c));;
    }
}
